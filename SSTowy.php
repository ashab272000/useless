				<p>
					Measuring 110.3 x 21.1 x 10.1ft, the "Towy" was powered by compound 2 cylinder coal fired engine, and grossed 204 tons, 83 tons net.<br>
    				Completed in April 1914 by Day, Summers & Co. Ltd., Southampton, for the Royal Mail Steam Packet Co., London, she was later sold to Ninian Shipping Co. Ltd., Cardiff on 28th May 1920; re-sold to Spillers Steamship Co. Ltd., Cardiff, on 6th April 1921, and finally sold to the Whiteabbey Shipping Co. Ltd., Co. Antrim on 19th March 1929.<br>
    				While leaving Portstewart for Irvine in ballast, she grounded.<br>
    				A tow to Portrush was organised, but she sank only a short distance from Portrush harbour on 20th June 1930.<br>
    				The "Towy" was the last trader to use the harbour at Portstewart.
