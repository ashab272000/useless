				<p>
					A 3 Masted iron vessel and built by Windgate on the Clyde in 1872, she weighed over 1216 net tons, and measured 300 x 35 x 24ft and had 2 decks.<br>
    				She was owned by Merrow & Co. who used her for shipping emigrants to America.<br>
    				On a trip from Glasgow to Larne/New York, she ran aground on Hunters Rock and was badly holed. The buoy marking the rock had been dislodged.<br>
    				Two tugs from Belfast and two from the Clyde could do little except take off the crew and the seventeen passengers, and some of the 2000 tons of cargo.<br>
    				Commanded by Captain McGowan.<br>
    				On January 13th, 1879, the big steamer cracked into three parts and sank.<br>
    				She was discovered by a local diving club in 1966.
				</p>
